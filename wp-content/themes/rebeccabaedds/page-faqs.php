            <button class="collapse_faqs__content__button"><p>Are amalgam (silver) fillings safe?</p></button>
            <div class="collapse_faqs__content__body">
                <div class="body_title">
                    <p>Over the years there has been some concern as to the safety of amalgam (silver) fillings. An
                        amalgam is a blend of copper, silver, tin and zinc, bound by elemental mercury. Dentists have
                        used this blended metal to fill teeth for more than 100 years. The controversy is due to claims
                        that the exposure to the vapor and minute particles from the mercury can cause a variety of
                        health problems. ‍</p>
                    <br/>
                    <p>According to the American Dental Association (ADA), up to 76% of dentists use silver containing
                        mercury to fill teeth. The ADA also states that silver fillings are safe and that studies have
                        failed to find any link between silver containing mercury and any medical disorder. ‍</p>
                    The general consensus is that amalgam (silver) fillings are safe. Along with the ADA’s position,
                    the Center for Disease Control (CDC), the World Health Organization, the FDA, and others support
                    the use of silver fillings as safe, durable, and cost effective. The U.S. Public Health Service
                    says that the only reason not to use silver fillings is when a patient has an allergy to any
                    component of this type of filling. The ADA has had fewer than 100 reported incidents of an
                    allergy to components of silver fillings, and this is out of countless millions of silver
                    fillings over the decades. ‍</p>
                    <br/>
                    <p> Although studies indicate that there are no measurable health risks to patients who have silver
                        fillings, we do know that mercury is a toxic material when we are exposed at high, unsafe
                        levels. For instance, we have been warned to limit the consumption of certain types of fish that
                        carry high levels of mercury in them. However, with respect to amalgam fillings, the ADA
                        maintains that when the mercury combines with the other components of the filling, it becomes an
                        inactive substance that is safe. ‍</p>
                    <br/>
                    <p> There are numerous options to silver fillings, including composite (tooth-colored), porcelain,
                        and gold fillings. We encourage you to discuss these options with your dentist so you can
                        determine which is the best option for you.
